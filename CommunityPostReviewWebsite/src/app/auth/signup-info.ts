export class SignUpInfo {
    name: string;
    username: string;
    email: string;
    password: string;
    countt: string;

    constructor(name: string, username: string, email: string, password: string, countt: string) {
        this.name = name;
        this.username = username;
        this.email = email;
        this.password = password;
        this.countt = countt;
    }
}
